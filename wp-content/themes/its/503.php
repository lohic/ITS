   	  <h3>&nbsp;Ses objectifs&nbsp;</h3>
      <p>Entretenir le patrimoine intellectuel du PSU et organismes proches, transmettre les valeurs et les idées du PSU et organismes proches dans les réflexions et les débats actuels.</p>

    	<h3>&nbsp;Son Fonctionnement&nbsp;</h3>
        <p><strong>L'Institut Tribune Socialiste est constitué sous la forme d'un fonds de dotation, conformément à l'article 140 de la loi n°2008-776 du 4 août 2008. </strong><br>
          Ce statut lui permet notamment de recevoir facilement des dons et des legs, et de faire appel à la générosité publique.<br>
          Il est géré par un Conseil d'administration, institué par les structures fondatrices&nbsp;:
          <ul>
              <li>l'association Les Amis de Tribune Socialiste (ATS),</li>
              <li>l'Institut Edouard Depreux (IED),</li>
              <li>l'Association des Amis de Victor et Paule Fay (AAVPF),</li>
              <li>l'association ESU-PSU, et UNEF ANNÉES 60.</li>
          </ul>
          </p>
      <p>Il s'appuie sur&nbsp;:
                    <ul>
              <li>un Comité de parrainage constitué par des anciens secrétaires nationaux du PSU,</li>
              <li>un Comité scientifique qui a pour fonction de conseiller et d'assister par ses avis et ses recommandations le conseil d'administration,</li>
              <li>un Collège des donateurs,</li>
              <li>des Groupes de travail et de réflexion animés par toutes celles et tous ceux qui souhaitent participer au travail de réflexion de l'Institut et à son action.</li>
          </ul>
        </p>

