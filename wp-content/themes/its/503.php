      <h2>Un centre d'archives et de documentation</h2>
      <p>(40 rue de Malte)
Le fonds historique s'articule autour d'une collection de « Tribune socialiste » (1960-1990), de PSU Documentation (1968-1986), et d'une base de données d'affiches PSU (Plus de 900 affiches sont actuellement répertoriées)
Il est en train de s'élargir à toutes les revues et publications du PS, à toutes les publications d'auteurs membres du PSU, à toutes les publications susceptibles de contribuer à la compréhension des idées du PSU, à l'accueil de fonds documentaires de militants du PSU
Il sera accessible en permanence.</p>
      <h2>Un site Internet</h2>
      <p><a href="http://www.institut-tribune-socialiste.fr">www.institut-tribune-socialiste.fr</a><br>
        Il permettra de conserver et de transmettre les archives, directement consultables, de communiquer sur les différentes activités de l'institut.<br>
      Plus de 2.000 documents seront  en accès direct : chronologies, biographies, iconographies, thèmes d'action et de réflexion du PSU...  croisant documents historiques et approches actuelles.</p>
      <h2>Des publications</h2>
      <p>En cours : une série de publications autour de thèmes et de textes historiques du PSU, aujourd'hui encore vivants (éventuellement accompagné de photos de tracts ou affiches), re-contextualisés, analysés, prolongés  par des contributions actuelles <br>
      * En rapport avec ces  publications : rencontres, séminaires, travaux de recherche...</p>
